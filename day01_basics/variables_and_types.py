
# Integers (int): Whole numbers
age = 30
print(f"Variable: age, Value: {age}, Type: {type(age)}")

# Floating-point numbers (float): Numbers with a decimal point
stock_price = 226.41
print(f"Variable: stock_price, Value: {stock_price}, Type: {type(stock_price)}")

# Complex numbers (complex): Numbers with a real and imaginary part
c_num = 1 + 2j
print(f"Variable: c_num, Value: {c_num}, Type: {type(c_num)}")


# Strings (str): Sequence of characters enclosed in quotes
name = "Alice"
print(f"Variable: name, Value: {name}, Type: {type(name)}")


# Booleans: Represents truth values (True or False)
is_active = True
print(f"Variable: is_active, Value: {is_active}, Type: {type(is_active)}")


# Lists (list): Ordered, changeable collection allowing duplicate members (uses square brackets)
my_list = [1, "two", 3.0, True]
print(f"Variable: my_list, Value: {my_list}, Type: {type(my_list)}")

# Tuples (tuple): Ordered, unchangeable collection (uses parentheses)
my_tuple = (1, "two", 3.0, True)
print(f"Variable: my_tuple, Value: {my_tuple}, Type: {type(my_tuple)}")

# Ranges (range): Represents an immutable sequence of numbers
my_range = range(5)
print(f"Variable: my_range, Value: {my_range}, Type: {type(my_range)}")


# Dictionaries (dict): Unordered collection of key-value pairs (uses curly braces)
my_dict = {"name": "Bob", "age": 25}
print(f"Variable: my_dict, Value: {my_dict}, Type: {type(my_dict)}")

# Sets (set): Unordered collection of unique items (uses curly braces)
my_set = {"apple", "banana", "cherry"}
print(f"Variable: my_set, Value: {my_set}, Type: {type(my_set)}")

# Frozensets (frozenset): Immutable version of a set
my_frozenset = frozenset({"apple", "banana", "cherry"})
print(f"Variable: my_frozenset, Value: {my_frozenset}, Type: {type(my_frozenset)}")


# NoneType: Represents the absence of a value
nothing = None
print(f"Variable: nothing, Value: {nothing}, Type: {type(nothing)}")
